// Contact form validation
const contactForm = document.getElementById("contactForm");
if (contactForm) {
  const nameInput = document.getElementById("name");
  const emailInput = document.getElementById("email");
  const telegramInput = document.getElementById("telegram");
  const instagramInput = document.getElementById("instagram");
  const contactMethodError = document.getElementById("contactMethodError");
  const formSuccess = document.getElementById("formSuccess");
  const submitBtn = document.getElementById("submitBtn");

  // Email regex
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;

  // Clear error state on input
  [nameInput, emailInput, telegramInput, instagramInput].forEach((input) => {
    if (input) {
      input.addEventListener("input", () => {
        input.classList.remove("error");
        const errorEl = input
          .closest(".form-group")
          ?.querySelector(".form-error");
        if (errorEl) errorEl.classList.add("hidden");
        contactMethodError.classList.add("hidden");
      });
    }
  });

  // Form submission
  contactForm.addEventListener("submit", (e) => {
    e.preventDefault();
    let isValid = true;

    // Reset states
    formSuccess.classList.add("hidden");
    contactMethodError.classList.add("hidden");
    submitBtn.disabled = true;
    submitBtn.classList.add("loading");

    // Validate name (required)
    if (!nameInput.value.trim()) {
      nameInput.classList.add("error", "shake");
      const nameError = nameInput
        .closest(".form-group")
        ?.querySelector(".form-error");
      if (nameError) nameError.classList.remove("hidden");
      isValid = false;
      setTimeout(() => nameInput.classList.remove("shake"), 500);
    } else {
      nameInput.classList.remove("error");
      nameInput.classList.add("success");
    }

    // Validate email format (if provided)
    const emailValue = emailInput.value.trim();
    if (emailValue && !emailRegex.test(emailValue)) {
      emailInput.classList.add("error", "shake");
      const emailError = emailInput
        .closest(".form-group")
        ?.querySelector(".form-error");
      if (emailError) emailError.classList.remove("hidden");
      isValid = false;
      setTimeout(() => emailInput.classList.remove("shake"), 500);
    } else if (emailValue) {
      emailInput.classList.remove("error");
      emailInput.classList.add("success");
    }

    // Validate at least one contact method
    const hasContact =
      emailValue || telegramInput.value.trim() || instagramInput.value.trim();
    if (!hasContact) {
      contactMethodError.classList.remove("hidden");
      [emailInput, telegramInput, instagramInput].forEach((input) => {
        input.classList.add("error", "shake");
        setTimeout(() => input.classList.remove("shake"), 500);
      });
      isValid = false;
    } else {
      if (telegramInput.value.trim()) telegramInput.classList.add("success");
      if (instagramInput.value.trim()) instagramInput.classList.add("success");
    }

    // If not valid, stop
    if (!isValid) {
      submitBtn.disabled = false;
      submitBtn.classList.remove("loading");
      return;
    }

    // Submit to Web3Forms
    const formData = new FormData(contactForm);

    fetch("https://api.web3forms.com/submit", {
      method: "POST",
      body: formData
    })
    .then(response => response.json())
    .then(data => {
      submitBtn.disabled = false;
      submitBtn.classList.remove("loading");
      if (data.success) {
        formSuccess.classList.remove("hidden");
        contactForm.reset();
        [nameInput, emailInput, telegramInput, instagramInput].forEach(
          (input) => {
            input.classList.remove("success", "error");
          },
        );
        // Hide success message after 5 seconds
        setTimeout(() => formSuccess.classList.add("hidden"), 5000);
      } else {
        console.error("Form submission error:", data);
        alert("Ошибка отправки. Попробуйте позже.");
      }
    })
    .catch(error => {
      submitBtn.disabled = false;
      submitBtn.classList.remove("loading");
      console.error("Form submission error:", error);
      alert("Ошибка отправки. Попробуйте позже.");
    });
  });
}

// Video autoplay on scroll
const stockVideo = document.getElementById("stockVideo");
const videoSection = stockVideo?.closest(".video-section");

if (stockVideo) {
  // Ensure video is muted (required for autoplay)
  stockVideo.muted = true;

  // Try to play immediately
  stockVideo.play().catch(() => {});

  // Also play on scroll into view
  if (videoSection) {
    const videoObserver = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            stockVideo.play().catch(() => {});
          } else {
            stockVideo.pause();
          }
        });
      },
      { threshold: 0 },
    );

    videoObserver.observe(videoSection);
  }

  // Fallback: play on any user interaction
  document.addEventListener(
    "click",
    () => {
      if (stockVideo.paused) {
        stockVideo.play().catch(() => {});
      }
    },
    { once: true },
  );

  document.addEventListener(
    "scroll",
    () => {
      if (stockVideo.paused) {
        stockVideo.play().catch(() => {});
      }
    },
    { once: true },
  );
}

